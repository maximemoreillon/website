<h1>Pet bottle collecting robot</h1>
<h2>Description</h2>
<p>
	This robot was designed as part of the 2014 STI robot competetion at EPFL.
  Its objective was to navigate through an arena to collect PET bottles and bring them to a collection point.
  This was achieved by designing the robot in the form of a cannon that would shoot bottles to the collection point without needing to physically move there.
</p>

