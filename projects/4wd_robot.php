<h1>4WD ROBOT</h1>
<p>
				Cheap four wheel drive robot chassis can easily be purchased from eBay.
				I got a hold of one and had two projects involving it.
				The first was part of a university project where the robot would be controlled by a raspberry pi, receiving commands through wifi from a Nintendo DS.
				The second project was a camera carrying robot controlled by an arduino and a remote control using an NRF24L01.
			</p>
