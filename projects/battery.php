<h1>Mobile battery</h1>
<h2>Project description</h2>
<p>
				This device consists of two 18650 lithium-ion batteries and a DC-DC converter and is used to provide power at various voltages on the go.
				It can provide 3.7V direclty from either one of the batteries, 5V from the DC-DC vonverter through USB ports or 7.4V by using the two batteries in series.
				The voltage of each battery can be shown on 7 segment displays mounted on the side of the enclosure.
				Charging the batteries can be done via a connector installed on the back of the unit.
				Additionally, the batteries can easily be replaced in case their life cycle were to have shortened too much.
			</p>
