<h1>Vocabulary practice web app</h1>
<p>
  When learning a new language, there is no way around having to learn its vocabulary.
  This simple web app was made to help with this process by providing a multiple answer question style UI.
  It was designed mainly for Japanese, which uses chinese characters that can have multiple readings.
</p>
<p>

</p>
<p>
  The app is built using a simple combination of HTML/CSS for the front end and PHP for the back end.
  The vocabulary is stored in a MySQL database.
</p>


