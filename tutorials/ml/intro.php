<h1>MACHINE LEARNING</h1>
<p>
				The classical approach get a robot to solve a certain problem is to program it with a sequence of all the successive steps necessary to succeed.
				Thus, the classical approach requires knowledge of the process the problem.
			</p>
<p>
				Machine learning (ML) differs from the classical approach in the sense that no knowledge of the process is required to achieve the desired output.
				Instead, ML optimizes the process so that the its output matches the desired output.
			</p>
<p>
				To perform such optimization, a measure of the performance is required so as to be maximized.
				For example, a dart throwing robot could measure its performance by computing the distance between the center of the target and where the dart landed.
				The robot could then refine the parameters of his throw so as to increase its performance for the next one.
			</p>
<p>
				Thus, whenever a measure of the process's performances is available, ML can be applied so as to optimize the process without requiring prior knowledge.
			</p>
