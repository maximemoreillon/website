<h1>ESP8266</h1>
<p>
    				There are various ways to provide a project with networking capabilities. One of the most popular is the ESP8266 wifi chip from Espressif.
    				The ESP8266 can be programmed directly using the Arduino IDE, thus eliminating the need for a separate microcontroller.
    			</p>
<h2> Breakout boards </h2>
<p>
    				To facilitate its use, the ESP8266 can be purchased already soldered to a breakout boards.
    				Among the many options that are available on the market, I recommend the Wemos D1 mini for its low price, small form factor and built-in USB port.
    				Also, the Wemos D1 mini is available from the Arduino IDE's board selection menu so no further configuration is required.
    			</p>
